     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if (auth()->check())
        {
            $blogs = Blog::select('blogs.*')
                        ->join('followers', 'blogs.user_id', '=', 'followers.user_id')
                        ->where('followers.follower_id', auth()->id())
                        ->with('user')
                        ->latest()
                        ->take(30)
                        ->get();

            if (count($blogs) == 0) {
                $blogs = Blog::latest()->with('user')->take(20)->get();
            }
        } else {
            $blogs = Blog::latest()->with('user')->take(20)->get();
        }

        return view('home', ['blogs' => $blogs]);
    }

